import React, { createContext, useContext, useEffect, useState } from 'react';
import { User, onAuthStateChanged, signInWithPopup, signOut } from 'firebase/auth';
import { auth, googleProvider } from '../services/firebase';

interface AuthContextType {
  currentUser: User | null;
  loading: boolean;
  signInWithGoogle: () => Promise<void>;
  logout: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const AuthProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [currentUser, setCurrentUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const unsubscribe = onAuthStateChanged(auth, (user) => {
      setCurrentUser(user);
      setLoading(false);
    });
    return unsubscribe;
  }, []);

  const signInWithGoogle = async () => {
    try {
      await signInWithPopup(auth, googleProvider);
    } catch (error: any) {
      console.error("Error signing in with Google", error);
      
      if (error.code === 'auth/unauthorized-domain') {
        alert(`⚠️ DOMINIO NO AUTORIZADO\n\nEl dominio actual (${window.location.hostname}) no tiene permiso para usar este proyecto de Firebase.\n\nSolución:\n1. Ve a Firebase Console -> Authentication -> Settings -> Authorized Domains.\n2. Agrega este dominio: ${window.location.hostname}`);
      } else if (error.code === 'auth/popup-blocked') {
        alert("El navegador bloqueó la ventana de inicio de sesión. Por favor permite las ventanas emergentes (popups) para este sitio e intenta de nuevo.");
      } else {
        alert(`Error al iniciar sesión: ${error.message}`);
      }
    }
  };

  const logout = async () => {
    try {
      await signOut(auth);
    } catch (error) {
      console.error("Error signing out", error);
    }
  };

  return (
    <AuthContext.Provider value={{ currentUser, loading, signInWithGoogle, logout }}>
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};